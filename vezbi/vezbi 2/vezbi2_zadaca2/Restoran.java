package vezbi2_zadaca2;

public class Restoran {
	private String imeNaRestoran;
	private String lokacija;
	private String telefon;
	private int brojNaSedista;
	
	public String getImeNaRestoran() {
		return imeNaRestoran;
	}
	public void setImeNaRestoran(String imeNaRestoran) {
		this.imeNaRestoran=imeNaRestoran;
	}
	
	public String getLokacija() {
		return lokacija;
	}
	public void setLokacija(String lokacija) {
		this.lokacija=lokacija;
	}
	
	public String getTelefon() {
		return telefon;
	}
	public void setTelefon(String telefon) {
		this.telefon=telefon;
	}
	
	public int getBrojNaSedista() {
		return brojNaSedista;
	}
	public void setBrojNaSedista(int brojNaSedista) {
		this.brojNaSedista=brojNaSedista;
	}
}
